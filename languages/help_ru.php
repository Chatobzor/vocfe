<h2>FAQ:</h2>
<ul>
    <li><b>Мне не дают войти в чат.</b><br>
        Не забывайте, что в нике применимы лишь буквы английского алфавита и знак _ (подчеркивания).
        Длина ника может быть от 3 до 15 символов.
        Посмотрите также на заглавной странице список присутствующих, нет ли там выбранного Вами ника.
        При этом учитывайте, что ники VanYA и Vanya считаются одинаковыми!
    <li><b>Да, там есть этот ник - но это я сам (пришлось перезайти)</b><br>
        Время, в течение которого пользователь считается живым - 3 минуты. Подождите, или зайдите под другим ником.<br>
    <li>
        <b>У меня оборвалась связь, когда я ее восстановил чат уже отключил меня. Теперь что, ждать остаток от 3 минут и только тогда заходить</b><br>
        Нет. Вы можете воспользоваться ссылкой &quot;<b>Перезайти</b>&quot;, находящейся вверху чата, для того чтобы избежать длительной процедуры.
        Если за время Вашего отсутствия под Вашим ником не зашел никто другой, Вы снова попадете в чат.
    <li><b>А что такое приват?</b><br>
        Приват - это сообщение, которое видно лишь двум посетителям чата - отправителю и адресату
    <li><b>Игнор... хм... что-то знакомое...</b><br>
        Нажав на &quot;-i&quot; напротив ника в правом окне, Вы больше не будете видеть сообщения, отправленые этим пользователем. Для возвращения - нажмите на появившуюся &quot;+v&quot;.
    <li><b>Я хочу отправить приват, но в моем списке нет ника адресата</b><br>
        Список
        &quot;Приват для:&quot; обновляется при каждой отправке Вами сообщения. Если Вы долго ничего не писали, и за это время
        в чат вошел нужный Вам человек, его ник не сможет появиться в Вашем списке. Отправьте какое-нибудь сообщение, или просто
        нажмите кнопку &quot;Отправить&quot; при пустой строке ввода сообщения.
    <li><b>Я отправил приват, но в центральном окне написано что он для неизвестного пользователя</b><br>
        Это значит, что адресат по каким-либо причинам покинул чат. Ваше сообщение он в любом случае прочесть уже не сможет.
    <li><b>А что это за надпись мелким шрифтом "кому-то шепчет"?</b><br>
        А это значит что этот человек отправил кому-то приватное сообщение. Но не Вам :)

    <li><b>А подробнее про Личку, плз</b><br>
        Личка - личная доска сообщений. В некотором роде - почта внутри чата.<br>
        Доступна только для зарегистрированных пользователей. Чтобы отправить сообщение на личку кому-либо, воспользуйтесь ссылкой
        &quot;<b>Отправить на личку</b>&quot;. Вам нужно будет заполнить поле &quot;Кому&quot; и &quot;Сообщение&quot;.
        Если Вы не уверены в правильном написании ника - воспользуйтесь предоставляемой возможностью поиска.<br>
        Вы также должны должны видеть надпись &quot;<u>Личка</u> - N сообщений&quot;.
        При нажатии на эту надпись, Вы попадаете в свою Личную доску объявлений, где можете читать/отвечать/удалять сообщения.
        После прочтения сообщения Вы сможете воспользоваться ссылкой &quot;Ответить&quot;.
        При нажатии на кнопку &quot;<b>Удалить</b>&quot;, выбранное сообщение будет стерто из системы.<br>

